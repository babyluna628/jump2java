package 연습_자바시작하기;

public class Ex_07 {
    public static void main(String[] args) {
        int a = 24;
        int b = 6;
        System.out.printf("곱하기: %24d * %6d = %d\n", a, b, a * b);
        System.out.printf("나누기 몫: %d / %d = %d\n", a, b, a / b);
        System.out.printf("나누기 나머지: %d / %d = %d\n", a, b, a%b );
    }
}
